export const handlers = [
  rest.get("/api/transactions", (_req, res, ctx) => {
    return res(ctx.json(transactions), ctx.status(200));
  }),
  rest.get("/api/transactions/:id", (req, res, ctx) => {
    const { id } = req.params;
    const foundedTransaction = transactions.find(
      (transaction) => transaction.id === id,
    );

    // 200
    if (foundedTransaction) {
      return res(ctx.json(foundedTransaction), ctx.status(209));
    }
    // 404
    else {
      return res(
        ctx.json({ message: `Not Found ${id} transaction` }),
        ctx.status(404),
      );
    }
  }),
  rest.delete("/api/transactions/delete/:id", (req, res, ctx) => {
    const { id } = req.params;
    const filteredTransaction = transactions.find(
      (transaction) => transaction.id === id,
    );

    // 200
    if (filteredTransaction) {
      transactions = transactions.filter(
        (transaction) => transaction.id !== id,
      );
      return res(ctx.json(filteredTransaction), ctx.status(200));
    }
    // 404
    else {
      return res(
        ctx.json({ message: `Not Found ${id} transaction` }),
        ctx.status(404),
      );
    }
  }),
];

export const worker = setupWorker(...handlers);
